int printPaperTapeChar (int ch, int (*putch)(int)) {
  char * font;
  int i=0;
  if (ch>0x30 && ch <= 0x39) {
    font = digits[ch-0x30];
  }
  else if (ch >= 0x41 && ch <= 0x5a) {
    font = letters[ch-0x41];
  }
  else if (ch >= 0x61 && ch <= 0x7a) {
    font = letters[ch-0x61];
  }
  else if (ch == 0x2d) {
    font = fontDash;
  }
  else if (ch == 0x2c) {
    font = fontComma;
  }
  else {
    return 0;
  }

  while (font[i]!=0) {
    (*putch)(font[i]);
    i++;
  }
  (*putch)(0);
  return 1;
}




